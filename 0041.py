a = int(input('Enter a: '))
b = int(input('Enter b: '))

print('{0} + {1} = {2}'.format(a,b,a+b))
print('{0} - {1} = {2}'.format(a,b,a-b))
print('{0} * {1} = {2}'.format(a,b,a*b))
print('{0} / {1} = {2}'.format(a,b,a/b))
print('{0} + {1} = {2}'.format(a**2,b**2,a**2 + b**2))
print('{0} + {1} = {2}'.format(a**3 ,b**3, a**3 + b**3))